                            <p>Nearly 95% of the Sidama live a life centered on agriculture. Customarily, the Sidama people have been agriculturalist and semi-pastoralist.  However, today, because of the high density of population and education, pastoral life is disappearing. The Sidama preserved their cultural heritage, including their traditional religion and language until the late 1880s during the conquest by Emperor Menelik II.  Before this, the Sidama had their own well-established administrative systems that dated at least to the 9th century, though it was made up of a loose coalition of Sidama kingdoms. As a result of marginalization and since the language did not have its own alphabet, very little has been written on Sidama issues. Many were not able to attend school until after the Derg came to power in 1975.  Today, the Sidama area has only a small number of schools, and inadequate health services, though primary education has increased recently.
                          </p>

                            <p>
The Sidama economy is based primarily on subsistence farming based on archaic production techniques. The important staple food is ENSET.  Other crops are also grown and they breed cattle. However, a substantial area of the Sidama land produces coffee, which is the major cash crop in the region. It supplies over 40% of washed coffee to the central market.  Coffee has been the major source of income for the rural households in the coffee producing regions of the Sidama land</p>

